
Tester le type d'une variable:
is.character(variable)
is.numeric(variable)
is.logical(variable)

Déclarer / créer des variables typées:
as.numeric(42)
as.numeric("42")
as.character("Hello")
as.character(42)

Anciennes version de R (similaires à la fonction as.numeric()):
  as.double()
  as.single()
  as.real()

